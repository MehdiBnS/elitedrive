    <h1 class="about-titre">Qui sommes nous ?</h1>
    <div class="about-section">
        <p>Bienvenue chez <span>EliteDrive</span>, l'agence de location de voitures de luxe par excellence, située dans le cadre prestigieux de Monaco. Nous ne sommes pas simplement une entreprise de location de véhicules ; nous sommes des créateurs d'expériences inoubliables, offrant à nos clients une gamme de voitures d’exception, de limousines somptueuses et de véhicules utilitaires haut de gamme.</p>

        <p>Chaque voiture de notre flotte incarne l’élégance, la performance et la sophistication. Nous sommes passionnés par l’automobile et cette passion se reflète dans chaque aspect de notre service. À <span>EliteDrive</span>, nous croyons que chaque voyage doit être une aventure inédite, un moment où le luxe rencontre le confort, où le design rencontre la puissance. Que vous soyez en mission professionnelle, en visite dans la mythique Principauté de Monaco, ou simplement en quête d’une expérience unique, nous avons le véhicule qui sublimera vos moments.</p>

        <p>Au-delà des voitures, <span>EliteDrive</span> incarne une vision, celle de rendre chaque déplacement exceptionnel. Nous vous offrons un service sur mesure, une expérience à la hauteur de vos attentes, dans le respect de la réputation de Monaco, capitale du luxe et de l’excellence.</p>
    </div>

    <h1 class="about-titre">Notre histoire</h1>
    <div class="about-section">
        <p>L’histoire de <span>EliteDrive</span> commence il y a plus d'une décennie, portée par la vision d'un entrepreneur passionné, qui rêvait de partager son amour pour l’automobile de luxe avec les visiteurs exigeants de Monaco. À l’image des grandes marques automobiles, notre fondateur a toujours eu un profond respect pour la perfection mécanique, l’innovation, et la quête constante de l’excellence. C’est dans cet esprit qu’il a décidé de créer une entreprise qui ne se contenterait pas de simplement fournir des voitures, mais qui offrirait des expériences uniques, où chaque détail compte.</p>

        <p>Né dans une famille d’entrepreneurs, notre fondateur a grandi dans le monde de l’automobile et a développé très tôt une fascination pour les véhicules d’exception. Après avoir travaillé dans l’industrie automobile de luxe et acquis une expertise précieuse, il a choisi de poser ses valises à Monaco, un lieu qui incarne à la perfection le luxe, l'élégance et la distinction. C’est ainsi qu'<span>EliteDrive</span> a vu le jour, avec la mission de transformer la location de véhicules en une expérience de prestige incomparable.</p>

        <p>Au fil des années, l'entreprise a évolué, mais son engagement envers la qualité et l’exclusivité n’a jamais faibli. Nous avons su agrandir notre flotte pour répondre aux besoins d'une clientèle internationale et exigeante, tout en préservant la même passion qui a animé notre création. Chaque voiture que nous proposons est soigneusement sélectionnée, testée et entretenue, pour offrir à nos clients une expérience parfaite à chaque trajet.</p>

        <p>Aujourd’hui, <span>EliteDrive</span> est bien plus qu’une simple agence de location de véhicules de luxe. Nous sommes une référence incontournable dans le domaine du transport haut de gamme à Monaco. Et notre aventure ne fait que commencer.</p>
    </div>

